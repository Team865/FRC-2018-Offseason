package ca.warp7.frc.core;

public enum StateType {
    /**
     * Reflects the input state of a component
     */
    ComponentInput,

    /**
     * Reflects the current state of a component
     */
    ComponentState,

    /**
     * Prints a line
     */
    Println,

    /**
     * Prints a warning
     */

    Warning,

    /**
     * Prints an error
     */
    Error
}
